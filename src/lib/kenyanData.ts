export const generateKenyanName = () => {
  const firstName = KENYAN_FIRST_NAMES[Math.floor(Math.random() * KENYAN_FIRST_NAMES.length)]
  const lastName = KENYAN_LAST_NAMES[Math.floor(Math.random() * KENYAN_LAST_NAMES.length)]
  return `${firstName} ${lastName}`
}

export const generateKenyanPhone = () => {
  const prefixes = ['07', '01']
  const prefix = prefixes[Math.floor(Math.random() * prefixes.length)]
  const suffix = Math.floor(Math.random() * 10000000).toString().padStart(7, '0')
  return `${prefix}${suffix}`
}

export const generateKenyanEmail = (name: string) => {
  const domains = ['gmail.com', 'yahoo.com', 'hotmail.com', 'outlook.com']
  const domain = domains[Math.floor(Math.random() * domains.length)]
  const cleanName = name.toLowerCase().replace(' ', '.')
  return `${cleanName}${Math.floor(Math.random() * 100)}@${domain}`
}
